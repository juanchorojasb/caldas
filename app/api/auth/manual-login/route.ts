import { NextRequest, NextResponse } from 'next/server'
import { compare } from 'bcryptjs'
import { db } from '@/lib/db'

export async function POST(request: NextRequest) {
  try {
    console.log('ğŸ“§ Manual login attempt')
    
    const body = await request.json()
    console.log('ğŸ“§ Body received:', body)
    
    const { email, password } = body
    
    if (!email || !password) {
      return NextResponse.json({ error: 'Missing credentials' }, { status: 400 })
    }
    
    console.log('ğŸ” Searching user:', email)
    
    const user = await db.user.findUnique({
      where: {
        email: email.toLowerCase(),
        isActive: true
      }
    })
    
    console.log('ğŸ‘¤ User found:', !!user)
    
    if (!user || !user.password) {
      return NextResponse.json({ error: 'User not found or no password' }, { status: 400 })
    }
    
    const passwordMatch = await compare(password, user.password)
    console.log('ğŸ” Password match:', passwordMatch)
    
    if (!passwordMatch) {
      return NextResponse.json({ error: 'Wrong password' }, { status: 400 })
    }
    
    console.log('âœ… LOGIN SUCCESS!')
    
    return NextResponse.json({ 
      success: true, 
      user: { id: user.id, email: user.email } 
    })
    
  } catch (error) {
    console.error('âŒ Error:', error)
    return NextResponse.json({ error: 'Server error' }, { status: 500 })
  }
}
